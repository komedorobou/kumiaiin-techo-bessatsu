export interface LeaveItem {
  name: string;
  type: string; // 有給・無給等
  days: string;
  conditions: string;
  unit?: string;
  notes?: string;
  eligibility?: string; // 正規/再任用/会計年度
  documents?: string;
  timeline?: TimelineStep[];
  table?: { headers: string[]; rows: string[][] };
  familyChart?: FamilyChartEntry[];
  subItems?: LeaveSubItem[];
}

export interface LeaveSubItem {
  label: string;
  value: string;
  note?: string;
}

export interface TimelineStep {
  label: string;
  duration: string;
  pay: string; // e.g. "100%", "80%", "無給"
  color: 'green' | 'yellow' | 'orange' | 'red' | 'gray';
}

export interface FamilyChartEntry {
  relation: string;
  days: number;
  note?: string;
  isBlood?: boolean; // 血族
}

export interface LeaveCategory {
  id: string;
  title: string;
  emoji: string;
  description: string;
  items: LeaveItem[];
}

